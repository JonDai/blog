(function() {

	var bodyEl = document.body,
		content = document.querySelector( '.content-wrap' ),
		opendream = document.getElementById( 'open-dream' ),
		openlife = document.getElementById( 'open-life' ),
		opentech = document.getElementById( 'open-tech' ),
		closebtn = document.getElementById( 'close-button' ),
		isOpen = false;
	function init() {
		initEvents();
	}

	function initEvents() {
		//open dream
		opendream.addEventListener( 'click', toggleMenu );
		if( closebtn ) {
			closebtn.addEventListener( 'click', toggleMenu );
		}

		// close the menu element if the target it´s not the menu element or one of its descendants..
		content.addEventListener( 'click', function(ev) {
			var target = ev.target;
			if( isOpen && target !== opendream ) {
				toggleMenu();
			}
		} );
		//open life
		openlife.addEventListener( 'click', toggleMenu );
		if( closebtn ) {
			closebtn.addEventListener( 'click', toggleMenu );
		}

		// close the menu element if the target it´s not the menu element or one of its descendants..
		content.addEventListener( 'click', function(ev) {
			var target = ev.target;
			if( isOpen && target !== openlife ) {
				toggleMenu();
			}
		} );
		//open tech
		opentech.addEventListener( 'click', toggleMenu );
		if( closebtn ) {
			closebtn.addEventListener( 'click', toggleMenu );
		}

		// close the menu element if the target it´s not the menu element or one of its descendants..
		content.addEventListener( 'click', function(ev) {
			var target = ev.target;
			if( isOpen && target !== opentech ) {
				toggleMenu();
			}
		} );
	}

	function toggleMenu() {
		if( isOpen ) {
			classie.remove( bodyEl, 'show-menu' );
		}
		else {
			classie.add( bodyEl, 'show-menu' );
		}
		isOpen = !isOpen;
	}

	init();

})();